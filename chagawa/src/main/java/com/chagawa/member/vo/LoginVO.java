package com.chagawa.member.vo;

public class LoginVO {
	
	private String id, pw, nickname, tel, status, gradeName;
	private long gradeNo;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getGradeName() {
		return gradeName;
	}
	public void setGradeName(String gradeName) {
		this.gradeName = gradeName;
	}
	public long getGradeNo() {
		return gradeNo;
	}
	public void setGradeNo(long gradeNo) {
		this.gradeNo = gradeNo;
	}
	
	
	@Override
	public String toString() {
		return "LoginVO [id=" + id + ", pw=" + pw + ", nickname=" + nickname + ", tel=" + tel + ", status=" + status
				+ ", gradeName=" + gradeName + ", gradeNo=" + gradeNo + "]";
	}
}
